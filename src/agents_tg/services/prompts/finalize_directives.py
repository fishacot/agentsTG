"""Structured finalizer prompt (E.3 adapted for Telegram HTML)."""

FINALIZE_USER_REPLY = """
# Финализатор для пользователя

## Цель
Создать итоговое сообщение, которое суммирует результаты выполнения задачи и представляет артефакты.

## Структура ответа (Telegram HTML)

<h2>✅ Готово</h2>
<p><b>Кратко:</b> {1–2 предложения о выполненных действиях}</p>

<h3>📄 Результаты</h3>
<ul>
<li>{артефакт: ссылка, имя файла, факт}</li>
</ul>

<h3>🔍 Детали</h3>
<p>{кратко: что сделано, без технических терминов}</p>

## Правила
1. Используй только факты из результатов инструментов и диалога — не выдумывай.
2. Не упоминай имена инструментов, JSON, внутренние форматы.
3. Если действий не было — короткий живой ответ без шаблона «Готово».
4. Не повторяй дословно содержимое артефактов в резюме.
5. Язык — русский, Telegram HTML (`<b>`, `<i>`, `<code>`, `<pre>`). Без Markdown.
"""


def build_finalize_prompt(*, has_tool_results: bool = False) -> str:
    """Return finalize system prompt; shorter when no tools were used."""
    if not has_tool_results:
        return (
            "Сформулируй финальный ответ пользователю на русском, в Telegram HTML. "
            "Не упоминай инструменты и JSON. "
            "Если было действие — кратко скажи результат и что дальше."
        )
    return FINALIZE_USER_REPLY.strip()
